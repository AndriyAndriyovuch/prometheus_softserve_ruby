"""
Створити метод remove_vowels(str) який видаляє всі голосні літери з набору, 
регістр символів не враховувати, до уваги взяти лише латинську абетку. 
Голосними вважати літери a,e,o,i,u. Метод повертає рядок з видаленими голосними літерами, 
при виклику методу з параметром-рядком де немає голосних літер – повертається початковий рядок.
"""

def remove_vowels(str)
    str.gsub(/[aeoiu]/i, "")
end
